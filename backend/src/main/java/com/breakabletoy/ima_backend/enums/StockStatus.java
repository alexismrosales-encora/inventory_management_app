package com.breakabletoy.ima_backend.enums;

/*
 * StockStatus enum defines the possible stock states for a product.
 *
 * IN_STOCK: Indicates that the product is available.
 * OUT_OF_STOCK: Indicates that the product is not available.
 */
public enum StockStatus {
    IN_STOCK,
    OUT_OF_STOCK,
}
